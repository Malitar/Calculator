/* ************************
 * MATH FUNCTIONS
************************ */

function add(num1, num2) {
    return num1+num2;
}

function sub(num1, num2){
    return num1-num2;
}

function multiply(num1, num2) {
    return num1*num2;
}

function divide(num1, num2) {
    return num1/num2;
}

console.log(add(1,2));
console.log(sub(1,2));
console.log(multiply(12, 2));
console.log(divide(4,2));